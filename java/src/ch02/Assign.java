package ch02;

public class Assign {
	public static void main(String[] args) {
		int a = 10, b = 20; //,(콤마) : 연속작업
		a += 5; // 10 + 5 = 15
		System.out.println(a);
		a -= 5; //15 - 5 = 10
		System.out.println(a);
		b *= 5; //20 * 5 = 100
		System.out.println(b);
		b /= 2; //100/2 = 50
		System.out.println(b);
	}
}
