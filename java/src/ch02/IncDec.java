package ch02;

//패키지 - 디렉토리(폴더)
//클래스 - 파일
public class IncDec {
	public static void main(String[] args) {
		int a = 10;
		System.out.println(a);
		a++; // 1증가
		//a = a + 1;
		System.out.println(a);

		int b = 10;
		System.out.println(b);
		b--; // 1감소
		// b = b + 1;
		System.out.println(b);
	}
}
